# Функция для получения буквы от пользователя
get_user_guess() {
    local guess
    while true; do
        read -p "Введите букву: " guess
        guess=$(echo "$guess" | tr '[:lower:]' '[:upper:]')
        
        if [[ $guess =~ ^[А-ЯЁ]$ ]]; then
            break
        else
            echo "Ошибка: пожалуйста, введите одну букву русского алфавита."
        fi
    done
    echo "$guess"
}

